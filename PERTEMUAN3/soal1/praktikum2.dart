void main() {
  // Langkah 1
  int counter = 0; // Deklarasi variabel dengan tipe data int

  // Menggunakan while loop untuk mencetak angaka dari 0 hingga 32
  while (counter < 33) {
    print(counter);
    counter++; // Increment variabel
  }

  // Langkah 3 
  // Menggunakan do-while loop untuk mencetak angka dari 33 hingga 76
  do {
    print(counter);
    counter++;
  } while (counter < 77);
}
